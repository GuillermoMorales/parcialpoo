package Demonios;

/**
 *
 * @author Guille
 */
public class NovenoCirculo {
     public int vida=600;
     public int alma=10000;
     public int pecado=5000;
     public int monedajudas=3000;
     

     
     //GETTERS
        public int getAlma() 
        {
            return alma;
        }
        public int getPecado()
        {
            return pecado;
        }
        public int getMonedajudas() 
        {
            return monedajudas;
        }
        public int getVida() 
        {
            return vida;
        }
    //SETTERS
        public void setAlma(int alma) 
        {
            this.alma = alma;
        }
        public void setPecado(int pecado) 
        {
            this.pecado = pecado;
        }
        public void setMonedajudas(int monedajudas)   
        {
            this.monedajudas = monedajudas;
        }   
        public void setVida(int vida)   
        {
            this.vida = vida;
        }
    
}
